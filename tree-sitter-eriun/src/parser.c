    case 21:
      ACCEPT_TOKEN(aux_sym__endComment_token1);
      END_STATE();
    case 22:
      ACCEPT_TOKEN(aux_sym__endComment_token1);
      if (lookahead == ' ') ADVANCE(23);
      if (lookahead == '-') ADVANCE(24);
      if (lookahead == '{') ADVANCE(26);
      if (('\t' <= lookahead && lookahead <= '\r')) ADVANCE(22);
      if (lookahead != 0) ADVANCE(21);
      END_STATE();
    case 23:
      ACCEPT_TOKEN(aux_sym__endComment_token1);
